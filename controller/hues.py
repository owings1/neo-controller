def hex_to_value(hexcode: str) -> tuple[int, int, int]:
  if hexcode[0] == '#':
    hexcode = hexcode[1:]
  return tuple(
    int((int(f'0x{h}') / 255) * 16)
    for h in (
      hexcode[:2],
      hexcode[2:4],
      hexcode[4:]))

